	请嘉宾来为比赛开球在足坛并不是什么新鲜事，可如果担任开球嘉宾的不是人类而是动物，那会是怎样的一番景象呢？在上周末（11月26日）进行的日本J联赛第33轮，大阪樱花主场迎战神户胜利船的比赛开始之前，主队就别出心裁地请来了一只可爱的小猴子为比赛开球。不过这一标新立异的行为随即引发了一片热议。</div>
<div>
	<br />
	在当天的比赛开始之前，一只身穿大阪樱花球衣的小猴子在饲养员的带领下走进场内，只见它先是对着面前的皮球踢了几脚，然后又将球交到了此役的主裁手中。这一&ldquo;敢想敢做&rdquo;的行为随后在日本各界引发一片争论，有不少球迷对此置之一笑，表示&ldquo;小猴子很可爱，大阪樱花这一安排很新颖、很有趣。&rdquo;但在日本国内的动物保护协会看来，这样的行为却并不值得提倡，他们认为小猴子其实并不愿意去做这种事，却要被迫愉悦人类，大阪樱花方面应该感到羞耻。</div>
<div>
	<br />
	不知是不是这一只可爱的小猴子为大阪樱花带来了好运，在当天的比赛中，主队尽管在开场不久后就遭遇丢球，但此后他们却连入3球完成逆转，在全取3分的同时也继续排名积分榜第3位，握有争夺下赛季亚冠席位的主动权。而此前，大阪樱花还拿下了本赛季日本联赛杯的冠军，当时他们在决赛中击败了由中村宪刚领衔的川崎前锋，这也是大阪樱花历史上获得的首个国内赛事冠军头衔。（新民晚报见习记者 陆玮鑫）</div>

